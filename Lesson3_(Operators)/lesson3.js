let num = 99;

switch (num) {
    case num < 99:
        console.log("Less 99");
        break;
    case num = 99:
        console.log("Equals 99");
        break;
    case num > 99:
        console.log("More 99");
        break;
    default:
        console.log("Something wrong");
}

